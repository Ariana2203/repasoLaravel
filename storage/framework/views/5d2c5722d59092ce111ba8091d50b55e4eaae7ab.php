<h1>Salvador Dalí</h1>
<br>
<p><img src="/images/dali.jpg" alt="">Salvador Felipe Jacinto Dalí nace en Figueras (España) en 1904. Entre 1921 y 1925 estudia en la Academia San Fernando de Madrid donde entabla amistad con el poeta Federico Garcia Lorca y el cineasta Buñuel. En 1925 la Galería Dalmau de Barcelona le organiza su primera exposición personal, exposición a partir de la cual Picasso y Miró empiezan a interesarse por sus trabajos.
<br>
Dalí se deja influir primero por el futurismo, por el cubismo después (1925). En abril de 1926 Dalí viaja por primera vez a Paris donde visita a Picasso. En su segundo viaje a París, en 1929, asiste al rodaje de la película de Buñuel “El perro Andaluz“ de la cual Dalí es coguionista, y Miró le presenta al grupo de los surrealistas. Dalí encuentra a André Breton y ……a Gala, su futura esposa y musa (casada en aquel entonces con Paul Eluard). Adhiere al movimiento surrealista en 1929. Dali se interesa por las teorías sicoanalíticas de Freud y define su método “paranóico-critico“. Pinta en aquel periodo espacios oníricos y fantasmagóricos poblados de elementos simbólicos : relojes blandos, muletas, animales fantásticos, personajes retorcidos. Dalí participa todavía a las manifestaciones y exposiciones surrealistas después de su exclusión en 1934. Dalí interpreta a su modo varias obras famosas, como el Ángelus de Millet, dando varias versiones de ellas. Breton le apoda ¡ “Ávida Dólar” !
<br>
Después de la guerra civil española se compromete políticamente a favor de Franco. A partir de los años 40 declara que quiere acercarse a la realidad y retorna a una expresión pictórica más clásica, sin dejar de imprimir en sus obras su fantasía personal.
