<h1>Pablo Picasso</h1> 
<br> 
<p><img src="/images/picasso.jpg" alt="">
Pablo Diego José Ruiz Picasso, nació en Málaga España, el 25 de octubre de 1881. Primer hijo del matrimonio entre José Ruiz Blasco y María Picasso López; no fueron sus habilidades como estudiante los que lo hicieron destacar precisamente, fue su facilidad para el dibujo que desde muy niño lo hizo resaltar.
<br>
El trabajo de su padre los llevó a trasladarse frecuentemente de lugar, primero a la Coruña y luego a Barcelona. Inicia este pequeño sus estudios de pintura en la Escuela de Bellas Artes de San Fernando en Madrid, en 1896, más adelante y tan solo con 15 años de edad, instala en la Ciudad Condal, su primer taller.
<br>
Picasso cosechando éxitos desde muy joven
<br>
Los éxitos empezaron a brotar desde sus 17 años, le fue otorgada una mención honorífica, gracias a su obra Ciencia y caridad, en la gran exposición de Madrid y también presentó su primera galería individual en Barcelona.
<br>
Tuvo la oportunidad de visitar París en 1900, para observar la Exposición Universal, un año más tarde dio inicio a lo que se le conoce como “período azul”, expone su primera muestra al siguiente año y se traslada a Paris definitivamente en 1904.
<br>
En 1907 pintó, Las señoritas de Aviñón, una obra experimental de las líneas maestras del cubismo. Al iniciar en 1914 la guerra mundial, abandonó el cubismo y fue en 1917 cuando se le asignó realizar los decorados del ballet Parade de Eric Satie, una gran responsabilidad que asumió con orgullo
<br>
Contrajo matrimonio con Olga Klochlova bailarina y quien trajo a este mundo su primogénito. Una relación que terminó a causa de su romance con Teresa y quien lo hizo padre, esta vez de una Maya, su pequeña hija. Posteriormente el amor volvió a aparecer con Dora Maar.
<br>
En 1937 pintó el Guernica en París, luego se vio obligado a trabajar durante la mayor parte del tiempo de la Segunda Guerra Mundial, en su refugio de Royan. Se afilió en 1944, al Partido Comunista francés y se hace nuevamente padre de dos pequeños, al conocer la pintora Françoise Guillot.
<br>
Su vida amorosa siguió su curso, esta vez en 1957 con Jacqueline Roqué y con quien compartió, hasta el día de su muerte en 1973.
